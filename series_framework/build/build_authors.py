# -*- coding: utf-8 -*-
"""
Build data/authors.json (+ demo/authors_data.js) from authors_data.{SF,FANTASY}.

- One primary genre per author. If a name appears in both lists, SF wins (it is
  listed as primary there); within a list, near-duplicates collapse.
- Each author gets a Goodreads author-search URL in the exact format supplied:
  https://www.goodreads.com/search?utf8=%E2%9C%93&q=<name>&search_type=books&search%5Bfield%5D=author
- Books/covers/photos are resolved live from OpenLibrary in the browser.
"""
import json, os, re, unicodedata
from urllib.parse import urlencode

HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)
from authors_data import SF, FANTASY


def slugify(s):
    s = unicodedata.normalize("NFKD", s).encode("ascii", "ignore").decode("ascii")
    return re.sub(r"[^a-zA-Z0-9]+", "-", s).strip("-").lower()


def norm(name):
    """Normalize for dedupe: ascii, lowercase, drop periods, drop single-letter
    middle initials, collapse spaces. So 'Raymond E. Feist' == 'Raymond Feist'."""
    s = unicodedata.normalize("NFKD", name).encode("ascii", "ignore").decode("ascii")
    s = s.lower().replace(".", " ")
    parts = [p for p in s.split() if len(p) > 1 or p.isdigit()]
    return " ".join(parts)


def gr_author_url(name):
    qs = urlencode({
        "utf8": "✓",
        "q": name,
        "search_type": "books",
        "search[field]": "author",
    })
    return "https://www.goodreads.com/search?" + qs


authors = []
seen = {}
for genre, lst in (("Science Fiction", SF), ("Fantasy", FANTASY)):
    for name, known_for in lst:
        key = norm(name)
        if key in seen:
            # keep first (SF primary); enrich known_for if missing
            continue
        seen[key] = True
        authors.append({
            "id": slugify(name),
            "name": name,
            "genre": genre,
            "known_for": known_for,
            "goodreads_url": gr_author_url(name),
        })

by_genre = {}
for a in authors:
    by_genre.setdefault(a["genre"], 0)
    by_genre[a["genre"]] += 1

out = {
    "meta": {
        "generated_by": "build_authors.py",
        "author_count": len(authors),
        "by_genre": by_genre,
        "genres": ["Science Fiction", "Fantasy"],
        "books_resolution": "each author's books, covers and photo are fetched from openlibrary.org at runtime; each book cross-links to Goodreads",
    },
    "authors": sorted(authors, key=lambda a: (a["genre"], a["name"])),
}

with open(os.path.join(ROOT, "data", "authors.json"), "w", encoding="utf-8") as f:
    json.dump(out, f, indent=2, ensure_ascii=False)
with open(os.path.join(ROOT, "demo", "authors_data.js"), "w", encoding="utf-8") as f:
    f.write("/* Auto-generated by build_authors.py — do not edit by hand. */\n")
    f.write("window.SFF_AUTHORS = ")
    json.dump(out, f, ensure_ascii=False)
    f.write(";\n")

print(f"authors: {len(authors)}  by genre: {by_genre}")
print("wrote data/authors.json and demo/authors_data.js")
