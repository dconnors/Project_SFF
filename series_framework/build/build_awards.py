# -*- coding: utf-8 -*-
"""
Build a Hugo / Nebula award index from readmore's data.json so the demo can show
consistent award badges and recognise award books across the series + author flows.

Output: data/awards.json and demo/awards_data.js (window.SFF_AWARDS).

The index is keyed by a normalized "title||author" book identity. The SAME
normalization is implemented in demo/unified.js (JS) — keep the two in sync.
"""
import json, os, re, unicodedata

HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)


def find_readmore():
    """Locate the readmore tree (holding site/data.json). Override via READMORE_DIR."""
    cands = [
        os.environ.get("READMORE_DIR"),
        os.path.join(ROOT, "..", "Project_Readmore", "mount-readmore-main"),
        os.path.join(ROOT, ".."),
        os.path.join(ROOT, "..", ".."),
    ]
    for c in cands:
        if c and os.path.exists(os.path.join(c, "site", "data.json")):
            return c
    return os.path.join(ROOT, "..", "Project_Readmore", "mount-readmore-main")


READMORE = find_readmore()

ARTICLES = ("the ", "a ", "an ")


def strip_accents(s):
    return unicodedata.normalize("NFKD", s).encode("ascii", "ignore").decode("ascii")


def norm_title(t):
    s = strip_accents(str(t)).lower().replace("&", " and ")
    s = re.sub(r"[^a-z0-9]+", " ", s).strip()
    s = re.sub(r"\s+", " ", s)
    for art in ARTICLES:
        if s.startswith(art):
            s = s[len(art):]
            break
    return s.strip()


def norm_author(a):
    s = strip_accents(str(a)).lower().replace(".", " ")
    toks = [t for t in re.split(r"\s+", s) if len(t) > 1 or t.isdigit()]
    return " ".join(toks)


def book_key(title, author):
    return norm_title(title) + "||" + norm_author(author)


data = json.load(open(os.path.join(READMORE, "site", "data.json")))
index = {}
for b in data.get("books", []):
    awards = b.get("awards") or {}
    if not awards:
        continue
    author = (b.get("authors") or [b.get("author_raw", "")])[0]
    key = book_key(b.get("title", ""), author)
    # keep the strongest status if duplicate keys collide
    entry = index.setdefault(key, {})
    for a, status in awards.items():
        st = "winner" if "winner" in str(status) else "nominee"
        if entry.get(a) != "winner":
            entry[a] = st

out = {
    "meta": {
        "generated_by": "build_awards.py",
        "source": "readmore site/data.json",
        "entries": len(index),
    },
    "index": index,
}

with open(os.path.join(ROOT, "data", "awards.json"), "w", encoding="utf-8") as f:
    json.dump(out, f, indent=2, ensure_ascii=False)
with open(os.path.join(ROOT, "demo", "awards_data.js"), "w", encoding="utf-8") as f:
    f.write("/* Auto-generated by build_awards.py — do not edit by hand. */\n")
    f.write("window.SFF_AWARDS = ")
    json.dump(out, f, ensure_ascii=False)
    f.write(";\n")

print(f"award index entries: {len(index)}")
print("wrote data/awards.json and demo/awards_data.js")
